export function detectLanguage(acceptLanguage?: string): Language {
  if (!acceptLanguage) return "de";
  const langs: Language[] = ["de", "en", "fr", "it"];
  for (const lang of langs) {
    if (acceptLanguage.toLowerCase().includes(lang)) return lang;
  }
  return "de";
}
